def sum(numberOne, numberTwo):
    return numberOne + numberTwo

def difference(numberOne, numberTwo):
    return numberOne - numberTwo

def product(numberOne, numberTwo):
    return numberOne * numberTwo

def quotient(numberOne, numberTwo):
    return numberOne / numberTwo

def modulus(numberOne, numberTwo):
    return numberOne % numberTwo

def squareroot(numberOne):
    return sqrt(numberOne)

